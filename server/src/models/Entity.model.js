const mongoose = require('mongoose');

const { Schema } = mongoose;
const EntitySchema = new Schema({
  entityName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

module.exports = mongoose.model('Entity', EntitySchema);